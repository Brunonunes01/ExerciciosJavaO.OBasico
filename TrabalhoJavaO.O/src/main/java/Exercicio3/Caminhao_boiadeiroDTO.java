package Exercicio3;

public class Caminhao_boiadeiroDTO extends Veiculo_transporteDTO{

    private int qtd_andares, qtd_divisorias, capacidade_tonelada, qtd_semireboque;

    public int getQtd_andares() {
        return qtd_andares;
    }

    public void setQtd_andares(int qtd_andares) {
        this.qtd_andares = qtd_andares;
    }

    public int getQtd_divisorias() {
        return qtd_divisorias;
    }

    public void setQtd_divisorias(int qtd_divisorias) {
        this.qtd_divisorias = qtd_divisorias;
    }

    public int getCapacidade_tonelada() {
        return capacidade_tonelada;
    }

    public void setCapacidade_tonelada(int capacidade_tonelada) {
        this.capacidade_tonelada = capacidade_tonelada;
    }

    public int getQtd_semireboque() {
        return qtd_semireboque;
    }

    public void setQtd_semireboque(int qtd_semireboque) {
        this.qtd_semireboque = qtd_semireboque;
    }

}
