package Exercicio3;

public class Caminhao_cegonhaDTO extends Veiculo_transporteDTO {

    private int qtd_andares, capacidade_carros, capacidade_tonelada;

    public int getQtd_andares() {
        return qtd_andares;
    }

    public void setQtd_andares(int qtd_andares) {
        this.qtd_andares = qtd_andares;
    }

    public int getCapacidade_carros() {
        return capacidade_carros;
    }

    public void setCapacidade_carros(int capacidade_carros) {
        this.capacidade_carros = capacidade_carros;
    }

    public int getCapacidade_tonelada() {
        return capacidade_tonelada;
    }

    public void setCapacidade_tonelada(int capacidade_tonelada) {
        this.capacidade_tonelada = capacidade_tonelada;
    }

}
