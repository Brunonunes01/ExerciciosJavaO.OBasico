public class AnimalDTO {
    private String raca;
    private int codigo_pai, codigo_mae, codigo;
    private double peso;

    public String getRaca() {
        return raca;
    }

    public void setRaca(String raca) {
        this.raca = raca;
    }

    public int getCodigo_pai() {
        return codigo_pai;
    }

    public void setCodigo_pai(int codigo_pai) {
        this.codigo_pai = codigo_pai;
    }

    public int getCodigo_mae() {
        return codigo_mae;
    }

    public void setCodigo_mae(int codigo_mae) {
        this.codigo_mae = codigo_mae;
    }

    public int getCodigo() {
        return codigo;
    }

    public void setCodigo(int codigo) {
        this.codigo = codigo;
    }

    public double getPeso() {
        return peso;
    }

    public void setPeso(double peso) {
        this.peso = peso;
    }
            
}
